package com.learn.classdesign;

public class AnimalGorilla {
    private int age;
    private String name;
    public AnimalGorilla(int age,String name){
        super();
        this.age = age;
        this.name = name;
    }
    public AnimalGorilla(int age){
        super();
        this.age=age;
        this.name=null;
    }
}
